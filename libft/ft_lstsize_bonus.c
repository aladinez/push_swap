#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	t_list	*lst1;
	int		i;

	lst1 = lst;
	i = 0;
	while (lst1)
	{
		lst1 = lst1->next;
		i++;
	}
	return (i);
}
